<script>
    document.addEventListener('DOMContentLoaded', function () {
        const commentForm = document.getElementById('commentForm');
        const comentariosDiv = document.getElementById('comentarios');

       
        const comentariosGuardados = JSON.parse(localStorage.getItem('comentarios')) || [];

        
        comentariosGuardados.forEach(function (comentarioGuardado) {
            mostrarComentario(comentarioGuardado);
        });

        commentForm.addEventListener('submit', function (e) {
            e.preventDefault();

            const usuario = document.getElementById('usuario').value;
            const correo = document.getElementById('correo').value;
            const comentarioTexto = document.getElementById('comentario').value;
            const fechaHora = new Date().toLocaleString();

            
            const comentario = {
                usuario: usuario,
                correo: correo,
                comentario: comentarioTexto,
                fechaHora: fechaHora,
            };

            
            comentariosGuardados.push(comentario);
            localStorage.setItem('comentarios', JSON.stringify(comentariosGuardados));

            
            mostrarComentario(comentario);

            
            commentForm.reset();
        });

        function mostrarComentario(comentario) {
            const comentarioHTML = `
                <div class="comment-card">
                    <h5 class="card-title">${comentario.usuario}</h5>
                    <p class="card-text">${comentario.comentario}</p>
                    <p class="card-text">${comentario.fechaHora}</p>
                </div>
            `;
            comentariosDiv.insertAdjacentHTML('beforeend', comentarioHTML);
        }
    });
</script>
